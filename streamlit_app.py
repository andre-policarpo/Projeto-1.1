# Inicializa√ß√£o da p√°gina
df = pd.DataFrame()

st.set_page_config(page_title='Dashboard de Gastos MIDR', page_icon='üìä', layout='wide')
st.title("üìä An√°lise de Gastos - MIDR")

tab1, tab2, tab3, tab4, tab5 = st.tabs(['In√≠cio','Vis√£o Geral','An√°lise por Intervalo de Tempo','An√°lise Comparativa','Sobre'])


# 1. Introdu√ß√£o e Carregamento de Arquivos
with tab1:
    # Texto de in√≠cio
    st.header('Bem Vindo(a)!')
    st.text('Este √© um sistema experimental dedicado √† an√°lise de gastos do Minist√©rio da Integra√ß√£o e do Desenvolvimento Regional.\n' \
    'Aqui, a ideia √© simples: o site recebe seu arquivo, dentro das especifica√ß√µes de formata√ß√£o, e exibe seus dados em diversos gr√°ficos comparativos ' \
    'que facilitam a visualiza√ß√£o e interpreta√ß√£o desses dados.\nIdealizado para receber informa√ß√µes de faturas de √°gua e energia, √© importante que os arquivos ' \
    'enviados sigam uma certa estrutura, afim de que a leitura seja realizada corretamente. A seguir, ser√° abordada a maneira na qual se espera que o arquivo enviado ' \
    'esteja formatado.')
